// Функції для арифметичних операцій
function add(a, b) {
  return a + b;
}

function sub(a, b) {
  return a - b;
}

function multi(a, b) {
  return a * b;
}

function divide(a, b) {
  return b !== 0 ? a / b : "Помилка! Ділення на нуль неможливе.";
}

// Головна функція калькулятора
function calculator() {
  // Отримуємо числа від користувача
  let num1 = +prompt("Введіть перше число:");
  let num2 = +prompt("Введіть друге число:");

  // Перевіряємо, чи введені значення є числами
  if (isNaN(num1) || isNaN(num2)) {
    console.log("Помилка! Будь ласка, введіть коректне число.");
    return;
  }

  // Отримуємо операцію від користувача
  let operation = prompt("Введіть арифметичну операцію (+, -, *, /):");

  let result;

  // Викликаємо відповідну функцію залежно від оператора
  switch (operation) {
    case "+":
      result = add(num1, num2);
      break;
    case "-":
      result = sub(num1, num2);
      break;
    case "*":
      result = multi(num1, num2);
      break;
    case "/":
      result = divide(num1, num2);
      break;
    default:
      console.log("Помилка! Невідома операція.");
      return;
  }

  // alert(`Результат: ${result}`);
  console.log(`Результат: ${result}`);
}

// Викликаємо калькулятор
calculator();

